package com.vstl.javaEncapsulation;

public class EncapsulationforAppSignUp {

	private String strUserName, strEmailID, strGender;
	private long longMobileNumber;
	private int intAge;
	
	
	//Getter and Setter methods 
	public String getStrUserName() {
		return strUserName;
	}
	public void setStrUserName(String strUserName) {
		this.strUserName = strUserName;
	}
	public String getStrEmailID() {
		return strEmailID;
	}
	public void setStrEmailID(String strEmailID) {
		this.strEmailID = strEmailID;
	}
	public long getLongMobileNumber() {
		return longMobileNumber;
	}
	public void setLongMobileNumber(long longMobileNumber) {
		this.longMobileNumber = longMobileNumber;
	}
	public int getIntAge() {
		return intAge;
	}
	public void setIntAge(int intAge) {
		this.intAge = intAge;
	}
	public String getStrGender() {
		return strGender;
	}
	public void setStrGender(String strGender) {
		this.strGender = strGender;
	}	
	
}
